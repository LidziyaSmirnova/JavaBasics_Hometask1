package by.epam.javabasics.part2;

public class Task1 {
    public static void main(String[] args){
        double x;
        double y;
        double sum;
        double dif;
        double mult;
        double div;

        x = -35.5;
        y = 0.985;

        sum = x+y;
        System.out.println("Сумма двух чисел = " + sum);

        dif = x-y;
        System.out.println("Разность двух чисел = " + dif);

        mult = x*y;
        System.out.println("Произведение двух чисел = " + mult);

        div = x/y;
        System.out.println("Частное двух чисел = " + div);

    }
}
